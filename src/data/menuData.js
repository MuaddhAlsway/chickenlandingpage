export const getLocalizedItem = (item, lang) => {
  if (!item) return null;
  return {
    ...item,
    name: item.name[lang] || item.name.en,
    description: item.description[lang] || item.description.en,
    longDescription: item.longDescription[lang] || item.longDescription.en,
    tag: item.tag[lang] || item.tag.en,
    category: lang === 'ar' ? (item.categoryAr || item.category) : item.category,
    ingredients: item.ingredients[lang] || item.ingredients.en,
    prepTime: item.prepTime[lang] || item.prepTime.en,
  };
};

